#delete item or list with del keyword
fruits = ["Apple", "Banana", "Cherry", "Kiwi"]
print("Before del: ", fruits)
del fruits[3] # delete ""Kiwi""

print("After del: ", fruits)

#you can also del the entire list as follows
del fruits

