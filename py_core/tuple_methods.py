"""Tuple Methos"""
# There are two popular tuple methods count() and index()
# The count() returns a number of times an item occurs in a tuple
# While index() searches the tuple and returns the index or position of the item if found

fruits = ("Apple", "Bananan", "Cherry", "Apple", "Orange", "Apple", "Lemon")

print(fruits.count("Apple")) # Returns 3
print(fruits.index("Cherry"))
